		<p>Margret Jackson is a seventh grade art teacher at Everett Meredith Middle School in Middletown, Delaware. Growing up as the only girl in a family with two brothers, she spent most of her free time, when not playing volleyball, drawing pictures of horses. She loved watching movies and documentaries about animals in order to draw them in motion. After a few years of toiling, her father decided it was time to enroll her in art classes at the local community center. Not long after receiving instruction, her art began to adapt to birds in flight, gliding over vast landscapes and diving down waterfalls. When the time came to select a major at the local university, she leapt at the chance to become a teacher and share her passion with younger generations. Now she spends her evenings, following dinner with her husband and son, online, searching for examples of young artists to share with her class. In the hopes that they too will pursue art and create beautiful things.</p>
